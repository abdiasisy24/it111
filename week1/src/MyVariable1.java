public class MyVariable1 {

    public static void main(String[] args) {

        String name = "Abdiasis";
//        After a statement, Don't forget the semi colon!
        int hours = 40;
//        We are assingning 40 to the hours variable
//        Please note, When we have a string value, It is nested in "".
//        When we are referring to numbers/integers, No quotes!!!
        System.out.println(name);
        System.out.println(hours);

        String building, tree, lake, ocean;
        tree = "Redwood";
        building = "Empire state";
        lake = "Lake superior";
        ocean = "Pacific ocean";

//        String building = "Empire stete", tree = "Maple", lake = "lake superior", ocean = "pacific ocean";

        int weeks, decades, months, years;
        weeks = 40;
        decades = 10;
        months = 3;
        years = 5;

//        int weeks = 4, decades = 10, months = 3, years = 5;

        System.out.println(tree);
        System.out.println(building);
        System.out.println(lake);
        System.out.println(ocean);
        System.out.println(weeks);
        System.out.println(decades);
        System.out.println(months);
        System.out.println(years);



    }

}
